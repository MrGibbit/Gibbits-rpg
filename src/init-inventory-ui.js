    consumeFoodFromInv,
    canEquip,
    equipAmmoFromInv,
    equipFromInv,
    setUseState,
    chatLine,
    lockManualDropAt,
    addGroundLoot,
    renderInv,
    withdrawFromBank,
    unequipSlot,
    getQuiverCount,
    moveAmmoFromQuiverToInventory,
    onRubXpLamp,
    depositFromInv,
    useState,
    handleUseOnSelf,
    tryItemOnItem,
    isStickyUseTool,
    emptyInvSlots
